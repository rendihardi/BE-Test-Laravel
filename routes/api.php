    Route::get('/me', [AuthController::class, 'me'])->name('me');

    Route::get('/users/paginated', [UserController::class, 'getAllPaginated']);
    // Route::apiResource('/users', UserController::class);
});
Route::apiResource('/users', UserController::class);
